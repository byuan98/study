package entity;

import java.io.Serializable;

public class Stulogin implements Serializable{
    private Integer sid;
    private String sname;
    private String spwd;
    
    public Stulogin(){
        
    }
    public Stulogin(String sname, String spwd){
        this.sname=sname;
        this.spwd=spwd;
    }
    public Stulogin(Integer sid, String sname, String spwd){
        this.sid=sid;
        this.sname=sname;
        this.spwd=spwd;
    }
    public Integer getSid() {
        return sid;
    }
    public void setSid(Integer sid) {
        this.sid = sid;
    }
    public String getSname() {
        return sname;
    }
    public void setSname(String sname) {
        this.sname = sname;
    }
    public String getSpwd() {
        return spwd;
    }
    public void setSpwd(String spwd) {
        this.spwd = spwd;
    }

    public String toString() {
        return "sid:"+this.sid+",sname:"+this.sname+",spwd:"+this.spwd;
    }
    
    
}
