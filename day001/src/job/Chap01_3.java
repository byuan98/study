package job;

import java.util.*;

public class Chap01_3 {

    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        System.out.println("请输入一个需要进行判断的正整数：");
        int number = input.nextInt();
        if (number <= 0) {
            System.out.println("0或负数没有奇偶性");
        } else if (number % 2 == 0) {
            System.out.println("您输入的数字是偶数");
        } else if (number % 2 != 0) {
            System.out.println("您输入的数字是奇数");
        }
    }
}
